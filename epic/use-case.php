		<h1>User Case</h1>
		<p>Objective: To connect with other developers.</p>
		<p>Description: Have a list of developer that he has collaborated with.</p>
		<p>Precondition: Linus is logged in on StackOverflow</p>
		<p>Postconditional: Linus chooses what devs to collaborate with from developer profiles.</p>

		<h1>The site interaction flow:</h1>
		<ol>
			<li>Logs into site.</li>
			<li>Uses search function to search for a developer.</li>
			<li>Finds a developer he wants to collaborate with.</li>
			<li>Uses the messaging function to contact the developer.</li>
			<li>Adds the developer profile to his list of developers to collaborate with.</li>
			<li>Both developers are able to view eachother's profiles.</li>
		</ol>

